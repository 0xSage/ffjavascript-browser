exports.exp = (F, base, e) =>{
    let res = F.one;
    let rem = bigInt(e);
    let exp = base;

    while (! rem.eq(bigInt.zero)) {
        if (rem.and(bigInt.one).eq(bigInt.one)) {
            res = F.mul(res, exp);
        }
        exp = F.square(exp);
        rem = rem.shiftRight(1);
    }

    return res;
};
